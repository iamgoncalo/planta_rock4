def run(cmd, cwd=None):
    print(f"$ {cmd}")
    r = subprocess.run(cmd, shell=True, cwd=cwd, capture_output=True, text=True)
    if r.stdout.strip(): print(r.stdout.rstrip())
    if r.stderr.strip(): print(r.stderr.rstrip(), file=sys.stderr)
    return r.returncode == 0


def patch_searchbar(root, stamp):
    """Garante que a searchbar se esconde em /v2/chat* (cobre /chat e /chat2)."""
    f = root / "frontend" / "components" / "v2" / "PlantaSearchBar.tsx"
    if not f.exists():
        print("  i PlantaSearchBar.tsx nao encontrado, skip")
        return
    c = f.read_text()
    if "startsWith('/v2/chat')" in c or 'startsWith("/v2/chat")' in c:
        print("  i searchbar ja esconde em /v2/chat* (cobre /chat2)")
        return
    shutil.copy2(f, f.with_suffix(f".tsx.bak.v28.{stamp}"))
    # adicionar usePathname se preciso
    if "usePathname" not in c and "import { useRouter } from 'next/navigation';" in c:
        c = c.replace("import { useRouter } from 'next/navigation';",
                      "import { useRouter, usePathname } from 'next/navigation';")
    if "const pathname = usePathname();" not in c and "const router = useRouter();" in c:
        c = c.replace("const router = useRouter();",
                      "const router = useRouter();\n  const pathname = usePathname();", 1)
    m = re.search(r"\n  return \(\n    <div\n      style=\{\{\n        position: 'fixed',", c)
    if m:
        guard = "\n  if (pathname && pathname.startsWith('/v2/chat')) return null;\n"
        c = c[:m.start()] + guard + c[m.start():]
        f.write_text(c)
        print("  OK searchbar esconde-se em /v2/chat*")
    else:
        print("  i nao encontrei o return da searchbar — verifica manualmente")


def patch_topbar_nav(root, stamp):
    """Adiciona item 'Chat' ao nav do TopBar (best-effort, varios formatos)."""
    f = root / "frontend" / "components" / "v2" / "TopBar.tsx"
    if not f.exists():
        print("  i TopBar.tsx nao encontrado, skip")
        return
    c = f.read_text()
    if "/v2/chat2" in c:
        print("  i nav ja tem Chat")
        return

    backup_made = False
    def bk():
        nonlocal backup_made
        if not backup_made:
            shutil.copy2(f, f.with_suffix(f".tsx.bak.v28.{stamp}"))
            backup_made = True

    # Formatos comuns de item de nav, ancorados em Pipelines (ultimo item)
    candidates = [
        # { href: '/v2/pipelines', label: 'Pipelines' }
        (re.compile(r"(\{\s*href:\s*['\"]/v2/pipelines['\"]\s*,\s*label:\s*['\"]Pipelines['\"]\s*\})"),
         "{ href: '/v2/chat2', label: 'Chat' }, \\1"),
        # { label: 'Pipelines', href: '/v2/pipelines' }
        (re.compile(r"(\{\s*label:\s*['\"]Pipelines['\"]\s*,\s*href:\s*['\"]/v2/pipelines['\"]\s*\})"),
         "{ label: 'Chat', href: '/v2/chat2' }, \\1"),
        # ['Pipelines', '/v2/pipelines']
        (re.compile(r"(\[\s*['\"]Pipelines['\"]\s*,\s*['\"]/v2/pipelines['\"]\s*\])"),
         "['Chat', '/v2/chat2'], \\1"),
        # ['/v2/pipelines', 'Pipelines']
        (re.compile(r"(\[\s*['\"]/v2/pipelines['\"]\s*,\s*['\"]Pipelines['\"]\s*\])"),
         "['/v2/chat2', 'Chat'], \\1"),
    ]
    for rx, repl in candidates:
        if rx.search(c):
            bk()
            c = rx.sub(repl, c, count=1)
            f.write_text(c)
            print("  OK nav item 'Chat' adicionado (apos Pipelines)")
            return

    # Fallback: tentar ancora generica em '/v2/shows'
    m = re.search(r"(\{\s*href:\s*['\"]/v2/shows['\"][^}]*\})", c)
    if m:
        bk()
        c = c[:m.start()] + "{ href: '/v2/chat2', label: 'Chat' }, " + c[m.start():]
        f.write_text(c)
        print("  OK nav item 'Chat' adicionado (antes de Shows)")
        return

    print("  i Nao encontrei o array de nav. Adiciona manualmente um item:")
    print("     { href: '/v2/chat2', label: 'Chat' }")


