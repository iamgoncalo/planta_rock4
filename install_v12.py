def sec(m): print(); print("="*68); print("  "+m); print("="*68)

def run(cmd, cwd=None):
    print(f"$ {cmd}")
    r = subprocess.run(cmd, shell=True, cwd=cwd, capture_output=True, text=True)
    if r.stdout.strip(): print(r.stdout.rstrip())
    if r.stderr.strip(): print(r.stderr.rstrip(), file=sys.stderr)
    return r.returncode == 0

def remove_bottomnav(root, stamp):
    """Remove BottomNav do frontend/app/layout.tsx (root layout)."""
    lp = root / "frontend" / "app" / "layout.tsx"
    if not lp.exists():
        print("  i layout.tsx (root) não encontrado")
        return False
    c = lp.read_text()
    original = c
    if "BottomNav" not in c:
        print("  i BottomNav já não está no layout · skip")
        return True
    shutil.copy2(lp, lp.with_suffix(f".tsx.bak.v12.{stamp}"))
    # Remove import
    c = re.sub(r"^import\s+BottomNav\s+from\s+['\"][^'\"]+['\"];?\s*\n?", "", c, flags=re.MULTILINE)
    # Remove uso
    c = re.sub(r"<BottomNav\s*/>\s*", "", c)
    c = re.sub(r"<BottomNav>\s*</BottomNav>\s*", "", c)
    if c == original:
        print("  WARN: BottomNav presente mas regex falhou")
        return False
    lp.write_text(c)
    print("  OK BottomNav removido do root layout")
    return True

