def sec(m): print(); print("="*68); print("  "+m); print("="*68)

def run(cmd, cwd=None):
    print(f"$ {cmd}")
    r = subprocess.run(cmd, shell=True, cwd=cwd, capture_output=True, text=True)
    if r.stdout.strip(): print(r.stdout.rstrip())
    if r.stderr.strip(): print(r.stderr.rstrip(), file=sys.stderr)
    return r.returncode == 0

def setup_favicon(root, stamp):
    """Copia LOGO/MAIN LOGO.svg para frontend/public/favicon.svg sem fundo branco."""
    src = root / "LOGO" / "MAIN LOGO.svg"
    if not src.exists():
        # Tentar variantes
        for cand in [root / "LOGO" / "MAIN_LOGO.svg",
                     root / "LOGO" / "main logo.svg",
                     root / "LOGO" / "MainLogo.svg"]:
            if cand.exists():
                src = cand; break
    if not src.exists():
        # Listar para debug
        logo_dir = root / "LOGO"
        if logo_dir.exists():
            print(f"  ! LOGO/ contem: {[p.name for p in logo_dir.iterdir()]}")
        print(f"  WARN: nao encontrei MAIN LOGO.svg em LOGO/ -- skip favicon")
        return False
    print(f"  Encontrei: {src.name}")
    svg = src.read_text(encoding="utf-8", errors="ignore")
    original_size = len(svg)
    # Remover possivel rect com fill branco (background)
    svg2 = re.sub(
        r"<rect[^>]*fill\s*=\s*[\"']\s*(#fff(?:fff)?|white)\s*[\"'][^>]*/>",
        "", svg, flags=re.IGNORECASE)
    svg2 = re.sub(
        r"<rect[^>]*fill\s*=\s*[\"']\s*(#fff(?:fff)?|white)\s*[\"'][^>]*>.*?</rect>",
        "", svg2, flags=re.IGNORECASE | re.DOTALL)
    # Remover style background
    svg2 = re.sub(r"background[^;\"]*:[^;\"]*(?:#fff(?:fff)?|white)[^;\"]*;?", "", svg2, flags=re.IGNORECASE)
    if len(svg2) != len(svg):
        print(f"  Removidos elementos de fundo branco ({original_size} -> {len(svg2)} B)")
    fav = root / "frontend" / "public" / "favicon.svg"
    fav.parent.mkdir(parents=True, exist_ok=True)
    if fav.exists():
        shutil.copy2(fav, fav.with_suffix(f".svg.bak.v17.{stamp}"))
    fav.write_text(svg2, encoding="utf-8")
    print(f"  OK favicon.svg escrito ({len(svg2)} B)")
    return True

def patch_root_layout(root, stamp):
    """Adiciona <link rel='icon' type='image/svg+xml' href='/favicon.svg' /> ao root layout."""
    lp = root / "frontend" / "app" / "layout.tsx"
    if not lp.exists():
        print("  WARN: root layout.tsx nao encontrado")
        return False
    c = lp.read_text()
    if "favicon.svg" in c:
        print("  i favicon.svg ja referenciado no layout · skip")
        return True
    shutil.copy2(lp, lp.with_suffix(f".tsx.bak.v17.{stamp}"))
    
    # Estrategia 1: Adicionar/expandir o objecto metadata.icons
    if "export const metadata" in c and "icons" not in c:
        c = re.sub(
            r"export const metadata\s*:\s*Metadata\s*=\s*\{",
            "export const metadata: Metadata = {\n  icons: { icon: '/favicon.svg' },",
            c, count=1)
        lp.write_text(c)
        print("  OK metadata.icons adicionado")
        return True
    
    # Estrategia 2: Adicionar <link> directamente no <head>
    if "<head>" in c and "favicon" not in c:
        c = c.replace("<head>",
                       "<head>\n        <link rel=\"icon\" type=\"image/svg+xml\" href=\"/favicon.svg\" />",
                       1)
        lp.write_text(c)
        print("  OK <link rel=icon> adicionado ao <head>")
        return True
    
    # Estrategia 3: Criar arquivo head.tsx no root
    head = root / "frontend" / "app" / "head.tsx"
    head.write_text('''export default function Head() {
  return (
    <>
      <link rel="icon" type="image/svg+xml" href="/favicon.svg" />
    </>
  );
}
''')
    print("  OK head.tsx criado")
    return True

