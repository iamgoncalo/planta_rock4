def section(msg):
    print()
    print("=" * 64)
    print("  " + msg)
    print("=" * 64)

def run(cmd, cwd=None, check=False):
    print(f"$ {cmd}")
    r = subprocess.run(cmd, shell=True, cwd=cwd, capture_output=True, text=True)
    if r.stdout.strip():
        print(r.stdout.rstrip())
    if r.stderr.strip():
        print(r.stderr.rstrip(), file=sys.stderr)
    return r.returncode == 0

