def section(msg):
    print()
    print("=" * 68)
    print("  " + msg)
    print("=" * 68)

def run(cmd, cwd=None):
    print(f"$ {cmd}")
    r = subprocess.run(cmd, shell=True, cwd=cwd, capture_output=True, text=True)
    if r.stdout.strip(): print(r.stdout.rstrip())
    if r.stderr.strip(): print(r.stderr.rstrip(), file=sys.stderr)
    return r.returncode == 0

def patch_main_py(root: Path) -> bool:
    """Adiciona imports + include_router + create_all dos novos modelos."""
    main_py = root / "app" / "main.py"
    if not main_py.exists():
        print("  ERRO: app/main.py não encontrado")
        return False
    content = main_py.read_text()

    # 1. Adicionar imports após a última linha "from app.routers.devices ..."
    devices_import = "from app.routers.devices import router as devices_router"
    if devices_import not in content:
        print("  ⚠ devices_router import não encontrado · main.py pode ter mudado")
        return False
    new_imports = (
        devices_import + "\n"
        "from app.routers.cleaning import router as cleaning_router\n"
        "from app.routers.staff import router as staff_router\n"
        "from app.routers.incidents import router as incidents_router\n"
        "from app.routers.weather import router as weather_router"
    )
    if "cleaning_router" in content:
        print("  ℹ routers já importados · skip imports")
    else:
        content = content.replace(devices_import, new_imports, 1)
        print("  ✓ imports adicionados")

    # 2. Adicionar include_router após devices_router
    include_devices = "app.include_router(devices_router)"
    if include_devices not in content:
        print("  ⚠ include_router(devices_router) não encontrado")
        return False
    new_includes = (
        include_devices + "\n"
        "    app.include_router(cleaning_router)\n"
        "    app.include_router(staff_router)\n"
        "    app.include_router(incidents_router)\n"
        "    app.include_router(weather_router)"
    )
    if "include_router(cleaning_router)" in content:
        print("  ℹ routers já incluídos · skip includes")
    else:
        content = content.replace(include_devices, new_includes, 1)
        print("  ✓ include_router adicionados")

    # 3. Adicionar import dos modelos DB para o create_all
    # No bloco "from app.models.db.sensors import ..."
    db_import_old = "from app.models.db.sensors import ClusterRef, Sensor, SensorHealth, MaintenanceLog, TerminalLog"
    db_import_new = (
        "from app.models.db.sensors import ClusterRef, Sensor, SensorHealth, MaintenanceLog, TerminalLog  # noqa\n"
        "            from app.models.db.operations import CleaningLog, StaffRoster, IncidentLog  # noqa"
    )
    if "from app.models.db.operations" in content:
        print("  ℹ DB models já importados · skip")
    elif db_import_old in content:
        content = content.replace(db_import_old, db_import_new, 1)
        print("  ✓ DB models de operations importados")
    else:
        print("  ⚠ não consegui encontrar bloco db_import — verifica main.py")

    main_py.write_text(content)
    return True

