def sec(m): print(); print("="*68); print("  "+m); print("="*68)

def run(cmd, cwd=None):
    print(f"$ {cmd}")
    r = subprocess.run(cmd, shell=True, cwd=cwd, capture_output=True, text=True)
    if r.stdout.strip(): print(r.stdout.rstrip())
    if r.stderr.strip(): print(r.stderr.rstrip(), file=sys.stderr)
    return r.returncode == 0

def patch_main(root):
    p = root / "app" / "main.py"
    if not p.exists(): return False
    c = p.read_text()
    if "telemetry_router" in c:
        print("  i routers v10 ja registados · skip")
        return True
    # Anchor: ultimo import
    candidates = [
        "from app.routers.pipelines import router as pipelines_router",
        "from app.routers.weather import router as weather_router",
    ]
    anc = next((a for a in candidates if a in c), None)
    if not anc:
        print("  WARN: nenhum anchor de import encontrado")
        return False
    c = c.replace(anc, anc + "\nfrom app.routers.telemetry import router as telemetry_router\nfrom app.routers.cleaning_calendar import router as cleaning_calendar_router", 1)
    # Include router
    inc_candidates = [
        "app.include_router(pipelines_router)",
        "app.include_router(weather_router)",
    ]
    inc = next((a for a in inc_candidates if a in c), None)
    if inc:
        c = c.replace(inc, inc + "\n    app.include_router(telemetry_router)\n    app.include_router(cleaning_calendar_router)", 1)
    p.write_text(c)
    print("  OK main.py patched")
    return True

def patch_v2_layout(root):
    """Injecta o PlantaChatbot no layout do /v2 se existir."""
    candidates = [
        root / "frontend" / "app" / "v2" / "layout.tsx",
        root / "frontend" / "app" / "v2" / "layout.jsx",
    ]
    lp = next((p for p in candidates if p.exists()), None)
    if not lp:
        print("  i nenhum layout do /v2 encontrado · skip")
        return True
    c = lp.read_text()
    if "PlantaChatbot" in c:
        print("  i PlantaChatbot ja no layout · skip")
        return True
    # Importar e renderizar
    import_line = "import PlantaChatbot from '@/components/v2/PlantaChatbot';"
    if "import" in c:
        # Inserir depois do primeiro import
        idx = c.find("\n", c.find("import"))
        c = c[:idx] + "\n" + import_line + c[idx:]
    else:
        c = import_line + "\n" + c
    # Renderizar antes do </> ou </div> final
    if "{children}" in c:
        c = c.replace("{children}", "{children}\n      <PlantaChatbot />", 1)
    lp.write_text(c)
    print(f"  OK PlantaChatbot injectado em {lp.name}")
    return True

