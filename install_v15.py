def run(cmd, cwd=None):
    print(f"$ {cmd}")
    r = subprocess.run(cmd, shell=True, cwd=cwd, capture_output=True, text=True)
    if r.stdout.strip(): print(r.stdout.rstrip())
    if r.stderr.strip(): print(r.stderr.rstrip(), file=sys.stderr)

def main():
    root = Path.cwd()
    tb = root / "frontend" / "components" / "v2" / "TopBar.tsx"
    if not tb.exists():
        print("ERRO: TopBar.tsx nao encontrado"); sys.exit(1)
    stamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    shutil.copy2(tb, tb.with_suffix(f".tsx.bak.v15.{stamp}"))
    data = base64.b64decode("".join(TOPBAR_B64))
    tb.write_bytes(data)
    print(f"  OK TopBar.tsx v15 ({len(data)} B)")
    print(f"  Hamburger ESQUERDA + drawer 340px slide-in + KPIs live + search + recentes")
    
    run("git add frontend/components/v2/TopBar.tsx", cwd=str(root))
    run('git commit -m "feat(v15): hamburger esquerda + drawer slide-in com KPIs live, search, recentes"', cwd=str(root))
    run("git push", cwd=str(root))
    
    print("")
    print("  Aguarda ~90s.")
    print("  Mobile: hamburger animado ESQUERDA -> drawer slide-in com KPIs ao vivo + search + recentes")
    print("  Desktop: nav linha como antes")

if __name__ == "__main__":
    main()
